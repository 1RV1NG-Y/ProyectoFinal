    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String buscar = this.txtNumHab.getText().trim();
        if(buscar.isEmpty()){
            JOptionPane.showMessageDialog(this, "Debe de escribir un numero de habitaci贸n");
        }else{
            String query = "SELECT nombre FROM huesped WHERE habitacion="+buscar+" AND estatus=1";
            conn.Consult(query);
            try {
                this.lbNombre.setText(conn.rs.getString(1));
            } catch (SQLException ex) {
                this.lbNombre.setText("Habitaci贸n no ocupada");
            }
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

