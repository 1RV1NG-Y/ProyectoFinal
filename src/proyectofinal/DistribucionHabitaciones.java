  public ResultSet obtenerHabitaciones(int piso) throws SQLException{
        String query = "SELECT num,tipo,estatus FROM habitacion WHERE piso="+Integer.toString(piso);
        conn.Consult(query);   
       return conn.rs;
  }
  
  public void limpiarBotones(){
       for (int i = 0; i < botones.size(); i++) 
               jPanel1.remove(botones.get(i));
  }
  
   public void paint(Graphics g){        
        super.paint(g);
           Toolkit t = Toolkit.getDefaultToolkit ();
           Image imagen[] = {t.getImage ("src/sencillaLibre.png"),t.getImage ("src/dobleLibre.png"),t.getImage ("src/tripleLibre.png"),
                                            t.getImage ("src/sencillaOcupada.png"),t.getImage ("src/dobleOcupada.png"),t.getImage ("src/tripleOcupada.png")};
           int x=0,y=1,cont=0;
            try {
                   limpiarBotones();
                ResultSet res = obtenerHabitaciones(this.cbPisos.getSelectedIndex()+1);            
                do{                    
                        int pos =  (res.getInt(3)==0) ? res.getInt(2)-1:res.getInt(2)+2;
                        g.drawImage (imagen[pos], 30+x,120*y, this);            
                        crearBoton(Integer.toString(res.getInt(1)),30+x,120*y,!res.getBoolean(3));
                        cont++;
                        x+=65;                        
                   if(x==65*7){
                       res.next();
                       pos =  (res.getInt(3)==0) ? res.getInt(2)-1:res.getInt(2)+2;
                       g.drawImage (imagen[pos], 30+x,180, this);     
                      crearBoton(Integer.toString(res.getInt(1)),30+x,180*y,!res.getBoolean(3));
                       y=2;
                       x=0;
                   }                  
                }while(res.next());
            } catch (SQLException ex) {}
         
   }

   public void crearBoton(String numH,int x,int y,boolean activar){      
         JButton boton = new JButton(numH);                
                        this.jPanel1.add(boton); 
                        boton.setLocation(x+17, y+17);
                        boton.setForeground(Color.black);
                        boton.setSize(30, 30);    
                        boton.setBackground(null);                       
                        boton.setBorder(null);                                     
                        boton.addActionListener(new ActionListener(){
                            public void actionPerformed(ActionEvent e){
                                    String num = e.getActionCommand();
                                    proyectofinal.Registro_Prueba.ventana.txtNum.setText(num);
                                   dispose();
                            }
                        });
                        boton.setEnabled(activar);
                        botones.add(boton);
   }
   
